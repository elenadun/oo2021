import java.util.*;
public class SetExample {
    public static void main(String[] args)
    {
        // Set demonstration using HashSet
        Set<String> hash_Set = new HashSet<String>();
 
        hash_Set.add("Esimene");
        hash_Set.add("Teine");
        hash_Set.add("Kolmas");
        hash_Set.add("Neljas");
        hash_Set.add("Viies");
 
        System.out.println(hash_Set);
    }
}